<td>No invoice needed</td>
<td>If no invoice is needed, the tax point will be the <b>date of supply</b>.</td>
</tr>
<tr>
<td>VAT invoice issued (within 14 days)</td>
<td>If a VAT invoice has been issued within 14 days of the supply, the tax point will be the <b>date of the invoice.</b></td>
</tr>
<tr>
<td>VAT invoice issued (after 14 days)</td>
<td>If a VAT invoice has been issued 15 days or more after the date of supply, the tax point will be the <b>date of supply</b>.</td>
</tr>
<tr>
<td>Payment received or VAT invoice issued in advance</td>
<td>If a payment is received or a VAT invoice is issued in advance of the supply being made, the tax point will be the <b>earlier of either the date payment is received or the invoice date</b>.</td>
</tr>
<tr>
<td>Payment received in advance</td>
<td>If a payment is received in advance of the supply being made and a VAT invoice has yet to be issued, the tax point will be the <b>date the payment is received</b>.</td>
</tr>
<tr>
<td>Cash accounting scheme</td>
<td>If you use the VAT ,&nbsp;the tax point is always the date the payment is received.</td>
